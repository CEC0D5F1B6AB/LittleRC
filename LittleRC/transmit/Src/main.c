void SendPacket(){
	static uint8_t buf[128];
	static int tag;
	
	for(int i=0;i<10;i++) buf[i] = i+0x26;
	if(tag >= 10){
		tag = 0;
		
		rpc = 1;
		buf[0] = 0x88;
	}
	
	SI_ENP_H(); //enable PA
	
	SI446X_SEND_PACKET(buf, SEND_LENGTH, SEND_CHANNEL, 6);
	
	sent++;
}

void RecvPacket(){
	static uint8_t buf[128];
	
	SI446X_READ_PACKET(buf, RECV_LENGTH);

	rx_rssi = buf[0];
	
	received++;
}

void Handle_SI_IRQ(){
	static uint8_t buf[128];
	
	SI446X_INT_STATUS(buf);	//clean TI && read status

	if(buf[3] & (1<<5)){	//PACKET_SENT_PEND
		SI_ENP_L(); //disable PA
		
		if(rpc){
			rpc = 0;
			SI446X_START_RX(RECV_CHANNEL, 0, RECV_LENGTH, 2, 2, 2);//switch to tx when failed
			//SI446X_RX_HOP(0x41, 0x0f, 0x62, 0x76, 0x08, 0x5B);
			
			//start timeout timer
			__HAL_TIM_SET_AUTORELOAD(&htim17,  delay + 5);
			__HAL_TIM_SET_COUNTER(&htim17,  0);
		}else{
			SendPacket();
		}
		
	}else if(buf[3] & (1<<4)){	//PACKET_RX_PEND
		//STOP timeout timer
		__HAL_TIM_SET_AUTORELOAD(&htim17,  0);
		delay = __HAL_TIM_GET_COUNTER(&htim17);
		
		//recv packet
		RecvPacket();
		//continue tx packet
		SendPacket();

	}else if(buf[5] & (1<<0)){	//SYNC_DETECT_PEND
		//read rssi
		tx_rssi = SI446X_GET_FRR_REG(FRR_D_READ);

	}
}

void Handle_ACK(){
	//STOP timer
	__HAL_TIM_SET_AUTORELOAD(&htim17,  0);
	
	//tx packet again
	SendPacket();
	
	//count delay
	//delay = MAX_TIMEOUT;
}

